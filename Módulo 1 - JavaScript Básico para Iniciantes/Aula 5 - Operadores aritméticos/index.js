/* Aula 5 - Operadores Aritméticos */

/* 
    Operadores aritméticos básicos:

        + = Adição / concatenação;
        - = Subtração;
        * = Multiplicação;
        / = Divisão;
        % = Resto da divisão;
        ** = Potenciação;
        ++ = Incremento;
        -- = Decremento 
    Exemplos:
*/


const num1 = 12;

const num2 = 6;

const numero = '1';

let num3 = 3;

console.log(num1 + num2); // Adição
console.log(numero + num2); // Concatenação
console.log(num1 - num2); // Subtração
console.log(num1 / num2); // Divisão
console.log(num1 % num2); // Resto da divisão
console.log(num1 ** num2); // Potenciação
console.log(++num3); // Incremento
console.log(--num3); // Decremento



