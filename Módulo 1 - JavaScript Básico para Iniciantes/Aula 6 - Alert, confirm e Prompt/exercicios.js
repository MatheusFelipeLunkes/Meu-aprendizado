/* Com o uso do prompt recebemos 2 entradas de valores numéricos digitados pelo usuário e faça um adição entre eles, após isso com o uso do alert exiba o resultado */

// "Resolvi tratar o dado diretamente na entrada que o usuário digitasse pois sabia que o tipo de dado era do tipo NUMBER."


